async function seedHotels() {
  console.log("Starting to seed hotels data...")

  try {
    for (const hotel of dummyHotels) {
      const docRef = await addDoc(collection(db, "hotels"), {
        ...hotel,
        createdAt: new Date(),
        updatedAt: new Date(),
      })
      console.log(`Added hotel: ${hotel.name} with ID: ${docRef.id}`)
    }

    console.log("Successfully seeded all hotels!")
  } catch (error) {
    console.error("Error seeding hotels:", error)
  }
}

seedHotels()
